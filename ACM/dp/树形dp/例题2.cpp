#include<stdio.h>
#include<vector>
#include<algorithm>
#include<string.h>
using namespace std;

#define N 1005
#define inf 0x3f3f3f3f
vector<int>g[N];

int Max[N];
int Num[N];
int n;
int dp_Num(int u,int pre)
{
    int k=g[u].size();
    Num[u]=1;
    for(int i=0; i<k; i++)
    {
        if(g[u][i]==pre)continue;
        Num[u]+=dp_Num(g[u][i],u);
    }
    return Num[u];
}

void dp_Max(int u,int pre)
{
    int k=g[u].size();
    int Mod = 0;
    for(int i=0; i<k; i++)
    {
        if(g[u][i]==pre)continue;
        dp_Num(g[u][i],u);
        dp_Max(g[u][i],u);
        Mod += Num[g[u][i]];
        Max[u]=max(Max[u],Num[g[u][i]]);
    }
    Max[u]=max(Max[u],n-Mod-1);
    return;
}

int main()
{
    while(scanf("%d",&n)==1)
    {
        int u,v;
        memset(Max,0,sizeof(Max));
        memset(Num,0,sizeof(Num));
        for(int i=1; i<n; i++)
        {
            scanf("%d%d",&u,&v);
            g[u].push_back(v);
            g[v].push_back(u);
        }
        int p=0;Max[0]=inf;
        dp_Max(1,0);
        for(int i=1; i<=n; i++)
        {
            if(Max[p]>Max[i])p=i;
        }
        printf("%d\n",p);
        for(int i=0;i<N;i++)g[i].clear();
    }
}
